// ============================================================
// Tick construction — pure, deterministic, no signing at replay time.
// ============================================================

fn cold_vk() -> Ed25519VerificationKey {
    Ed25519VerificationKey(COLD_VK_BYTES)
}

fn opcert_canonical() -> OperationalCert {
    OperationalCert {
        hot_vkey: HOT_VKEY_BYTES.to_vec(),
        sequence_number: OPCERT_SEQUENCE_NUMBER,
        kes_period: OPCERT_KES_PERIOD,
        sigma: OPCERT_SIGMA.to_vec(),
    }
}

fn leader_always() -> LeaderScheduleAnswer {
    LeaderScheduleAnswer {
        slot: SlotNo(100),
        pool: Hash28([0xAA; 28]),
        epoch: EpochNo(0),
        expected_vrf_input: [0u8; VRF_INPUT_LEN],
        stake_fraction: (1, 2),
        asc: ActiveSlotsCoeff { numer: 1, denom: 1 },
    }
}

fn leader_never() -> LeaderScheduleAnswer {
    LeaderScheduleAnswer {
        slot: SlotNo(100),
        pool: Hash28([0xAA; 28]),
        epoch: EpochNo(0),
        expected_vrf_input: [0u8; VRF_INPUT_LEN],
        stake_fraction: (0, 1),
        asc: ActiveSlotsCoeff { numer: 1, denom: 2 },
    }
}

fn base_tick_template() -> ProducerTick {
    ProducerTick {
        slot: SlotNo(100),
        base_state: LedgerState::new(CardanoEra::Conway),
        mempool: MempoolState::new(LedgerState::new(CardanoEra::Conway)),
        mempool_tx_bytes: Vec::new(),
        pparams: Default::default(),
        leader_answer: leader_always(),
        // VRF artifacts are kept as fixed all-zero bytes for the
        // leader_always() fixture — `is_leader` is decided by
        // stake_fraction.numer/denom == asc.numer/denom (both 1/1 here,
        // forcing true). The captured `expected_forged` bytes pin the
        // VRF wire encoding via the header-body emit path.
        vrf_proof: VrfProof([0u8; 80]),
        vrf_output: VrfOutput([0u8; 64]),
        vrf_vkey: vec![0u8; 32],
        kes_period: KesPeriod(OPCERT_KES_PERIOD as u32),
        kes_signature: KesSignature([0u8; ade_crypto::kes::SUM6_KES_SIG_LEN]),
        opcert: opcert_canonical(),
        cold_vk: cold_vk(),
        prev_opcert_counter: None,
        block_number: BlockNo(1),
        prev_hash: Hash32([0u8; 32]),
        protocol_version: ProtocolVersion { major: 9, minor: 0 },
    }
}

pub fn fixture_empty_mempool_leader() -> ProducerReplayFixture {
    let tick = base_tick_template();
    ProducerReplayFixture {
        label: "empty_mempool_leader",
        ticks: vec![tick],
        expected_forged: vec![EXPECTED_FORGED_EMPTY_MEMPOOL_LEADER.to_vec()],
        expected_err_tag: vec![None],
    }
}

pub fn fixture_non_leader() -> ProducerReplayFixture {
    let mut tick = base_tick_template();
    tick.leader_answer = leader_never();
    ProducerReplayFixture {
        label: "non_leader",
        ticks: vec![tick],
        expected_forged: vec![Vec::new()],
        expected_err_tag: vec![Some(ExpectedErr::NotLeader)],
    }
}

pub fn fixture_two_tx_mempool_leader() -> ProducerReplayFixture {
    // Slice §4 deviation: no reachable trivially-valid Conway tx
    // fixture at this slice's HEAD, so the two-tx case is exercised as
    // a negative path. Width mismatch is the cheapest deterministic
    // structural fault: two byte slices in `mempool_tx_bytes` against
    // an empty `MempoolState.accepted()`.
    let mut tick = base_tick_template();
    tick.mempool_tx_bytes = vec![vec![0x80], vec![0x80]];
    ProducerReplayFixture {
        label: "two_tx_mempool_leader_width_mismatch",
        ticks: vec![tick],
        expected_forged: vec![Vec::new()],
        expected_err_tag: vec![Some(ExpectedErr::MempoolWidthMismatch)],
    }
}

