// Core Contract:
// - Deterministic: same inputs + same seed => byte-identical outputs
// - No wall-clock time, true randomness, HashMap/HashSet, or floats
// - Encode invariants in types
// - Explicit state transitions only
// - Canonical serialization for all persisted/hashed data

//! In-source replay fixtures for PHASE4-N-C S3 / CE-N-C-3.
//!
//! Every fixture is a deterministic `ProducerTick` value built from fixed
//! 32-byte seeds. Signed artifacts (`VrfProof`, `KesSignature`,
//! `OpCert.sigma`) are baked in as byte-literal constants so the replay
//! path never invokes RED signing. The regen helper at
//! `crates/ade_testkit/tests/regen_producer_fixtures.rs` recomputes the
//! artifact bytes via the RED wrappers (`ade_runtime::producer::signing`)
//! and prints them; a human pastes the printed bytes into this file.
//!
//! Currently three fixtures:
//!
//! - `fixture_empty_mempool_leader` — positive: empty-mempool leader
//!   tick; expected forged bytes captured below.
//! - `fixture_non_leader` — negative: same opcert / VRF artifacts as the
//!   leader fixture but the `LeaderScheduleAnswer.stake_fraction = (0,1)`
//!   so `is_leader_for_vrf_output` returns false.
//! - `fixture_two_tx_mempool_leader` — negative (degraded per slice doc
//!   §4): two malformed tx bytes that fail the width check against the
//!   empty `MempoolState`. Proves the structural-consistency gate. The
//!   positive two-tx case is deferred to a slice that has reachable
//!   trivially-valid Conway txs (S4 / S7 fixture-set expansion).

